.macro FFT8_FN name, no_perm
function celt_tx_fft8_\name\()_neon, export=1 // LIBOPUS DELTA (P)
        SETUP_LUT       \no_perm
        LOAD_INPUT      0, 1, 2, 3, x2, \no_perm

        LOAD_SUBADD
        FFT8            v0, v1, v2, v3

